package dsa.leetcode;

public class ArraysPractice {
    public static void main(String[] args) {
        int[] intArray = new int[7];

        intArray[0] = 12;
        intArray[1] = 55;
        intArray[2] = -12;
        intArray[3] = 34;
        intArray[4] = 55;
        intArray[5] = 67;
        intArray[6] = -7;

        for (int i = 0; i < intArray.length; i++) {
            System.out.println(intArray[i]);
        }
    }
}
